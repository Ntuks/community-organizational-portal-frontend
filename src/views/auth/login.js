const mapDispatchToProps = (dispatch) => ({
  startLogin: (loginObj) => dispatch(startLogin(loginObj)),
  login:(loginObj) => dispatch(login(loginObj))
});

export default connect(undefined, mapDispatchToProps)(SignIn);
